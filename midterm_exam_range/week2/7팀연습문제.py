# 연습문제9번


import turtle as t

t.shape("turtle")

t.width(10)
t.speed(0)
t.color("blue")
t.up()
t.goto(-220, 0)
t.down()
t.circle(100)
t.color("black")
t.up()
t.goto(0, 0)
t.down()
t.circle(100)
t.color("red")
t.up()
t.goto(220, 0)
t.down()
t.circle(100)
t.color("yellow")
t.up()
t.goto(-110, -100)
t.down()
t.circle(100)
t.color("green")
t.up()
t.goto(110, -100)
t.down()
t.circle(100)
t.done()
